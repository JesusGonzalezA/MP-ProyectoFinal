#include "UtilidadesFicheros.h"

#include <string>
#include <iostream>
#include <fstream>

using namespace std;

//****************************************************************************
//Control
//****************************************************************************
//----------------------------------------------------------------------------
// Devuelve true si existe un fichero con el nombre "nombre".
// Devuelve false en caso contrario
bool ExisteFichero (const string & nombre)
{
	//Abro el fichero
	ifstream fichero (nombre);

	//La funcion fail() devolverá true sino existe porque no lo puede abrir
	bool problema = fichero.fail();

	//Si lo abro lo cierro
	if (!problema) fichero.close();

	return !problema;
}
